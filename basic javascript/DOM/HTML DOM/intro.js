//HTML DOM (Document Object Model)
/**
 * ketika sebuah web page di-load, browser akan membuat sebuah Document Object
 * Model dari sebuah page
 * 
 * HTML DOM model dibentuk sbeagai sebuah pohon objects (tree of objects)
 * 
 * dengan object model, javascript mendapatkan semua yang dibutuhkan untuk 
 * membuat dynamic HTML
 * 
 * - javascript dapat merubah semua element HTML didalam page
 * - javascript dapat merubah semua attribute HTML dalam sebuah paga
 * - javascript dapat merubah semua CSS styles pada sebuah page
 * - javascript dapat mengeluarkan element HTML yang ada dan juga attributenya
 * - javascript dapat bereaksi terhadapat semua events HTML dalam page
 * - javascript dapat membuat HTML events baru didalam page
 */

