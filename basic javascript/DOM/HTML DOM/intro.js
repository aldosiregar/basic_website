//apa itu DOM
/**
 * DOM adalah sebuah W3C (World Wide Web Consortium) standard
 * 
 * DOM mendefinisikan standard untuk mengakses document
 * 
 * "W3C Document Object Model (DOM) adalah sebuah platform dan language-neutral
 * interface yang memperbolehkan programs dan scripts untuk secara dinamis 
 * mengakses dan memperbarui content, struktur, dan style dari sebuah dokumen"
 * 
 * W3C DOM standard dibagi menjadi 3 bagian
 * 
 * - core DOM   : standard model untuk semua tipe document
 * - XML DOM    : standard model untuk dokumen XML
 * - HTML DOM   : stnadard model untuk dokumen HTML
 */

//apa itu HTML DOM
/**
 * HTML DOM adalah sebuah standard object model dan programming interface untuk 
 * HTML, ia mendefinisikan
 * 
 * - HTML elements sebagai objects
 * - properties dari semua elements HTML
 * - methods untuk mengakses semua element HTML
 * - events untuk semua element HTML
 * 
 * dengan kata lain : HTML DOM adlah standard untuk bagaimana mengambil, merubah,
 * menambah, atau menghapus element HTML
 */